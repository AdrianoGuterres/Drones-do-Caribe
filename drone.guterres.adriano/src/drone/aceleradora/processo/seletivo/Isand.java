package drone.aceleradora.processo.seletivo;

public class Isand {
	
	private int limitX;
	
	private int  limitY;
	
	
	public Isand(int limitX, int limitY) {
		setLimitX(limitX);
		setLimitY(limitY); 
	}


	public int getLimitX() {
		return limitX;
	}


	public void setLimitX(int limitX) {
		this.limitX = limitX;
	}


	public int getLimitY() {
		return limitY;
	}


	public void setLimitY(int limitY) {
		this.limitY = limitY;
	}
	
	
	

}
